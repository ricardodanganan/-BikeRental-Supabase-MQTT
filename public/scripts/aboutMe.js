// Function to open the About Me overlay
function openAboutMeOverlay() {
  // Get the 'About-Me-Overlay' element by ID and set its display property to 'block'
  document.getElementById('About-Me-Overlay').style.display = 'block';
}

// Function to close the About Me overlay
function closeAboutMeOverlay() {
  // Get the 'About-Me-Overlay' element by ID and set its display property to 'none'
  document.getElementById('About-Me-Overlay').style.display = 'none';
}
