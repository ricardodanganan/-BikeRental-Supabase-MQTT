// Function to open the login form overlay
function openLoginOverlay() {
  // Set the display property of the 'login-overlay' element to 'block', making it visible
  document.getElementById('login-overlay').style.display = 'block';
}

// Function to close the login form overlay
function closeLoginOverlay() {
  // Set the display property of the 'login-overlay' element to 'none', hiding it
  document.getElementById('login-overlay').style.display = 'none';
}
